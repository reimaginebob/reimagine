#!/usr/bin/env node
// scripts/write-build-meta.mjs
// Stale-build self-healing (Foundation, 2026-05-26): writes the current
// commit SHA to BOTH `public/version.json` (served at /version.json and
// read at runtime by the version-check hook) AND `src/build-meta.js`
// (imported by App.jsx so the bundle knows its own identity). Both files
// are written from a single SHA read in one script run, so the bundled
// constant and the deployed JSON are provably consistent by construction.
//
// Runs as the FIRST step of the prebuild chain (see package.json) so that
// every subsequent step and the Vite build itself can read the generated
// files. Both generated files are gitignored; they are produced fresh on
// every build.
//
// SHA resolution order:
//   1. VERCEL_GIT_COMMIT_SHA   (set in production / preview deploys)
//   2. `git rev-parse HEAD`    (local development with git context)
//   3. 'dev-' + Date.now()     (CI tarball builds, npm pack, etc.)
// The fallback never throws; the build must not break in environments
// without git.

import fs from 'node:fs'
import path from 'node:path'
import { execSync } from 'node:child_process'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const REPO_ROOT = path.resolve(__dirname, '..')

function resolveSha() {
  const fromVercel = process.env.VERCEL_GIT_COMMIT_SHA
  if (fromVercel && fromVercel.trim()) return fromVercel.trim()
  try {
    const fromGit = execSync('git rev-parse HEAD', { cwd: REPO_ROOT, stdio: ['ignore', 'pipe', 'ignore'] }).toString().trim()
    if (fromGit) return fromGit
  } catch {}
  return 'dev-' + Date.now()
}

const sha = resolveSha()
const builtAt = new Date().toISOString()
const payload = { sha, builtAt }

const publicDir = path.join(REPO_ROOT, 'public')
const srcDir = path.join(REPO_ROOT, 'src')
fs.mkdirSync(publicDir, { recursive: true })
fs.mkdirSync(srcDir, { recursive: true })

const versionJsonPath = path.join(publicDir, 'version.json')
const buildMetaJsPath = path.join(srcDir, 'build-meta.js')

fs.writeFileSync(versionJsonPath, JSON.stringify(payload) + '\n')
fs.writeFileSync(
  buildMetaJsPath,
  `// Auto-generated by scripts/write-build-meta.mjs. Do not edit.\n` +
  `// Regenerated on every build; gitignored.\n` +
  `export const BUILD_SHA = ${JSON.stringify(sha)}\n` +
  `export const BUILT_AT = ${JSON.stringify(builtAt)}\n`
)

console.log(`write-build-meta: sha=${sha.slice(0, 7)} builtAt=${builtAt}`)
