import fs from 'node:fs'
import path from 'node:path'

const GUIDE_DIR = path.join(process.cwd(), 'src/data/user-guide')
const OUT_FILE = path.join(process.cwd(), 'src/data/user-guide-content.js')

const files = fs.readdirSync(GUIDE_DIR)
  .filter(f => f.endsWith('.md'))
  .sort()

const content = files
  .map(f => `# ${f}\n\n${fs.readFileSync(path.join(GUIDE_DIR, f), 'utf-8')}`)
  .join('\n\n---\n\n')

const escaped = JSON.stringify(content)

fs.writeFileSync(
  OUT_FILE,
  `// Auto-generated by scripts/build-user-guide.mjs. Do not edit by hand.\nexport const USER_GUIDE_CONTENT = ${escaped}\n`,
)

console.log(`Wrote ${content.length} chars to ${OUT_FILE}`)
