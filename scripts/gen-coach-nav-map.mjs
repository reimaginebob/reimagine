// Generator: writes src/coach-nav-map.js from FEATURE_MAP + NAV_LABELS.
// Run with `npm run gen:coach-nav-map` after changing FEATURE_MAP
// (src/coach-routing.js) or NAV_LABELS (src/nav-labels.js). The prebuild gate
// (scripts/check-coach-nav-map.mjs) fails the build if this output is stale, so
// renames in either source can't silently desync from the coach prompt.
//
// The map is emitted as a JSON-stringified string literal (not a template
// literal) so any punctuation in the labels/descriptions is escaped safely.
import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { renderCoachNavMap } from './lib/render-coach-nav-map.mjs'

const OUT = fileURLToPath(new URL('../src/coach-nav-map.js', import.meta.url))

const header =
  '// @generated by `npm run gen:coach-nav-map` — DO NOT EDIT BY HAND.\n' +
  '// Source: FEATURE_MAP (src/coach-routing.js) + NAV_LABELS (src/nav-labels.js).\n' +
  '// Prebuild gate scripts/check-coach-nav-map.mjs fails if this is stale.\n' +
  "// Imported by api/coach.js (.js extension — the api/* bundler can't trace .mjs).\n"

const body = `export const COACH_NAV_MAP = ${JSON.stringify(renderCoachNavMap())}\n`

writeFileSync(OUT, header + body)
console.log(`gen-coach-nav-map: wrote ${OUT}`)
